    //查询按钮触发事件
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            DBConn db = new DBConn();
            String sql = "";
            int index = jComboBox1.getSelectedIndex(); //获得下拉列表的下标值
            String message = jTextField1.getText(); //获得输入条件
            if (index == 1) {
                //按ID查询
                sql = "select * from department where ID = '" + message + "'";
            } else {
                //按Name查询
                sql = "select * from department where Name = '" + message + "'";
            }            
            ResultSet rs = db.Search(sql);
            if (rs.next()) {
                jTextField2.setText(rs.getString("ID"));
                jTextField3.setText(rs.getString("Name"));
                jTextField4.setText(rs.getString("Director"));
                jTextField5.setText(rs.getString("Number"));
            }else{
                JOptionPane.showMessageDialog(null, "没有该部门信息！！");
            }
            db.close();
        } catch (SQLException ex) {
            Logger.getLogger(DeleteDepartment.class.getName()).log(Level.SEVERE, null, ex);
        }       
    }//GEN-LAST:event_jButton1ActionPerformed

