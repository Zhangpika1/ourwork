    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int option = JOptionPane.showConfirmDialog(null, "确定要删除这些信息?","系统提示",JOptionPane.YES_NO_OPTION);
        int result = 0;
        if (option == JOptionPane.YES_OPTION){
             DBConn db = new DBConn();
            if(jComboBox1.getSelectedIndex() == 1){
                result = db.Update("delete from department where ID = '" + jTextField1.getText() + "'");
            }else
                result = db.Update("delete from department where Name = '" + jTextField1.getText() + "'");
            if(result != 0)
                JOptionPane.showMessageDialog(null, "删除成功！！"); 
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
        }
        else
            this.setDefaultCloseOperation(MainFrame.DO_NOTHING_ON_CLOSE);
    }//GEN-LAST:event_jButton2ActionPerformed

    
