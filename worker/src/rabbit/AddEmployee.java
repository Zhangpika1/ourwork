    private void initComboBox(){
        Vector v = new Vector();
        DBConn db = new DBConn();
        try{
            ResultSet rs = db.Search("select distinct name from department");
            while(rs.next()){
                v.addElement(rs.getString("name")); //添加到向量尾部，大小加1
            }
           dcm = new DefaultComboBoxModel(v);
        }catch(SQLException ex){
            ex.printStackTrace();
        }finally{
            db.close();
        }
    }

