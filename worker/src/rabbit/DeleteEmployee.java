    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
          DBConn db = new DBConn();
         try {
            String sql = "";
            index = jComboBox1.getSelectedIndex(); //获得下拉列表的下标值
            message = jTextField1.getText(); //获得输入条件
            if (index == 1) {
                //按ID查询
                sql = "select * from employee where ID = '" + message + "'";
            } else {
                //按Name查询
                sql = "select * from employee where name = '" + message + "'";
            }
            ResultSet rs = db.Search(sql);
            if (rs.next()) {
                jTextField2.setText(rs.getString("ID"));
                jTextField3.setText(rs.getString("name"));
                jTextField4.setText(rs.getString("sex"));
                jTextField5.setText(rs.getString("age"));
                jTextField6.setText(rs.getString("department"));
                jTextField7.setText(rs.getString("position"));
                jTextField8.setText(rs.getString("phone"));
                jTextField9.setText(rs.getString("time"));
                jTextField10.setText(rs.getString("address"));

            }else{
                JOptionPane.showMessageDialog(null, "没有该员工信息！！");
            }
            db.close(); //及时关闭数据库资源
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

